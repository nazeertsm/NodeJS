function concatStrings(strA, strB){

    return strA +strB;
}

console.log(concatStrings("abc", "def")); //"abcdef"
console.log(concatStrings("world", "wide")); // "worldwide" 
console.log(concatStrings("123", "456")); //"123456"
console.log(concatStrings(1, 2)); // 3 


